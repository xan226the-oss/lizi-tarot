function internalStyleReferences(cardId: number) {
  if (cardId === 1) return [];
  if (cardId === 49) return [1];
  if (cardId === 69) return [1, 49];
  if (cardId >= 2 && cardId <= 22) return [1, 49];
  if (cardId === 23) return [1, 69];
  if (cardId >= 24 && cardId <= 36) return [23, 1];
  if (cardId >= 37 && cardId <= 50) return [49, 1];
  if (cardId === 51) return [1, 49];
  if (cardId >= 52 && cardId <= 64) return [51, 1];
  return [69, 1];
}

type SuitKey = keyof typeof SUIT_SYSTEM;

function getSuit(cardId: number): SuitKey | null {
  if (cardId >= 23 && cardId <= 36) return "wands";
  if (cardId >= 37 && cardId <= 50) return "cups";
  if (cardId >= 51 && cardId <= 64) return "swords";
  if (cardId >= 65 && cardId <= 78) return "pentacles";
  return null;
}

function getSuitSymbol(symbolDescription: string | null) {
  if (!symbolDescription) return null;
  if (symbolDescription.includes("潮汐容器或记忆透镜")) return "潮汐容器或记忆透镜";
  const symbols = ["火种脉杆", "彗火导体", "潮汐容器", "记忆透镜", "誓光刃", "晶体切面", "星核印", "轨道矿石"];
  return symbols.find((symbol) => symbolDescription.includes(symbol)) ?? null;
}

function getSampleForbidden(cardId: number): readonly string[] {
  if (cardId === 1) return SAMPLE_FORBIDDEN[1];
  if (cardId === 49) return SAMPLE_FORBIDDEN[49];
  if (cardId === 69) return SAMPLE_FORBIDDEN[69];
  return [];
}

const entries: TarotArtManifestEntry[] = rows.map(([
  cardId,
  archetype,
  sceneSummary,
  characters,
  location,
  dominantColor,
  symbolDescription,
  uprightVisualCue,
  reversedVisualCue
]) => {
  const suit = getSuit(cardId);
  const suitRules = suit ? SUIT_SYSTEM[suit] : null;
  const rankNumber = suit ? ((cardId - 23) % 14) + 1 : null;
  const requiredSymbolCount = rankNumber !== null && rankNumber <= 10 ? rankNumber : null;

  return {
    cardId,
    archetype,
    sceneSummary,
    characters: [characters],
    location,
    dominantColor,
    suitSymbol: getSuitSymbol(symbolDescription),
    requiredSymbolCount,
    uprightVisualCue,
    reversedVisualCue,
    internalStyleReferences: internalStyleReferences(cardId),
    requiredElements: [
      ...GLOBAL_REQUIRED,
      ...(symbolDescription
        ? requiredSymbolCount === null
          ? ["至少 1 个清晰花色符号", `卡面符号要求：${symbolDescription}`]
          : [`卡面必须清晰呈现 ${symbolDescription}`]
        : [])
    ],
    forbiddenElements: [
      ...GLOBAL_FORBIDDEN,
      ...(suitRules?.forbidden ?? []),
      ...getSampleForbidden(cardId)
    ],
    librarySymbols: suitRules
      ? [suitRules.symbol]
      : ["星门、潮幕、相位机构、活种光路与原创文明结构"]
  };
});

export const tarotArtManifest: TarotArtManifestEntry[] = entries.sort(
  (left, right) => left.cardId - right.cardId
);

export function getTarotArtManifestEntry(cardId: number) {
  return tarotArtManifest.find((entry) => entry.cardId === cardId) ?? null;
}
